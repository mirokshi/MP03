package model;



/**
 *
 * @author mirokshi
 */
public class person {

    private String name;
    private String surname;
    private int age;
    private int phone;

    //getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    @Override
    public String toString() {
        return "Staff{" + "name=" + name + ", surname=" + surname + ", age=" + age + ", phone=" + phone + '}';
    }
    
}
